    private void Button_empezarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Button_empezarActionPerformed
        // TODO add your handling code here:
        names.clear();
        
        if(TextField_primer_nombre.getText().length() != 0){
            names.add(TextField_primer_nombre.getText());
        }
        
        if(TextField_segundo_nombre.getText().length() != 0){
            names.add(TextField_segundo_nombre.getText());
        }
        
        if(TextField_tercer_nombre.getText().length() != 0)
            names.add(TextField_tercer_nombre.getText());
        
        if(TextField_cuarto_nombre.getText().length() != 0)
            names.add(TextField_cuarto_nombre.getText());
            
        if(names.size() < 2)
            JOptionPane.showMessageDialog(this, "Error con el numero de jugadores introducido.", MainWindow.getInstance().getAppName(), JOptionPane.ERROR_MESSAGE);
        else{
            dispose();
        }
    }//GEN-LAST:event_Button_empezarActionPerformed


