    void setLoot(LootToUI loot) {
        valor_armas.setText(Integer.toString(loot.getnWeapons()));
        valor_escudos.setText(Integer.toString(loot.getnShields()));
        valor_hangar.setText(Integer.toString(loot.getnHangars()));
        valor_fuel.setText(Integer.toString(loot.getnSupplies()));
        valor_medallas.setText(Integer.toString(loot.getnMedals()));

        if (loot.isGetEfficient()) {
            valor_transformacion.setText("Estaci√≥n Eficiente");
        } else if (loot.isSpaceCity()) {
            valor_transformacion.setText("Ciudad Espacial");
        } else {
            valor_transformacion.setText("No");
        }
        
        repaint();
        revalidate();

    }

